package pe.gob.mininter.dirandro.util;

import java.util.Date;

public class FormBandejaTrabajo {
	
	private String autogenerado;
	private String caso;
	private String tipoHecho;
	private String dependencia;
	private String tipoFinalidad;
	private String integrante;
	private Date fechaRegInicio;
	private Date fechaRegFinal;
	private Integer diasInicio;
	private Integer diasFinal;
	private String lugarHecho;
	private String jurisdiccion;
	
	public String getAutogenerado() {
		return autogenerado;
	}
	public void setAutogenerado(String autogenerado) {
		this.autogenerado = autogenerado;
	}
	public String getCaso() {
		return caso;
	}
	public void setCaso(String caso) {
		this.caso = caso;
	}
	public String getTipoHecho() {
		return tipoHecho;
	}
	public void setTipoHecho(String tipoHecho) {
		this.tipoHecho = tipoHecho;
	}
	public String getDependencia() {
		return dependencia;
	}
	public void setDependencia(String dependencia) {
		this.dependencia = dependencia;
	}
	public String getTipoFinalidad() {
		return tipoFinalidad;
	}
	public void setTipoFinalidad(String tipoFinalidad) {
		this.tipoFinalidad = tipoFinalidad;
	}
	public String getIntegrante() {
		return integrante;
	}
	public void setIntegrante(String integrante) {
		this.integrante = integrante;
	}
	public Date getFechaRegInicio() {
		return fechaRegInicio;
	}
	public void setFechaRegInicio(Date fechaRegInicio) {
		this.fechaRegInicio = fechaRegInicio;
	}
	public Date getFechaRegFinal() {
		return fechaRegFinal;
	}
	public void setFechaRegFinal(Date fechaRegFinal) {
		this.fechaRegFinal = fechaRegFinal;
	}
	public Integer getDiasInicio() {
		return diasInicio;
	}
	public void setDiasInicio(Integer diasInicio) {
		this.diasInicio = diasInicio;
	}
	public Integer getDiasFinal() {
		return diasFinal;
	}
	public void setDiasFinal(Integer diasFinal) {
		this.diasFinal = diasFinal;
	}
	public String getLugarHecho() {
		return lugarHecho;
	}
	public void setLugarHecho(String lugarHecho) {
		this.lugarHecho = lugarHecho;
	}
	public String getJurisdiccion() {
		return jurisdiccion;
	}
	public void setJurisdiccion(String jurisdiccion) {
		this.jurisdiccion = jurisdiccion;
	}

}
