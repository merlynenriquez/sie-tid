	public void setPericia(Pericia pericia) {
		this.pericia = pericia;
		
		if( periciaService.buscar(pericia).size()!=0){
			pericia = periciaService.buscar(pericia).get(0);
			btnRegistrar.setVisible(false);
			label_1.setValue("La pericia a esta muestra ya ha sido solicitada.");
			btnCancelar.setCaption("Cerrar");
		}
		
		String muestra = pericia.getMuestra().getDroga()!=null?"Droga: ":"Especie: " ;
		muestra += pericia.getMuestra().getDroga()!=null? HarecUtil.marcaModeloNombreToEmpty( pericia.getMuestra().getDroga().getTipoDroga() ) : pericia.getMuestra().getEspecie().getNombre();
		
		lblMuestra.setValue( muestra );
		lblEstado.setValue( "Estado de la Pericia: "+ pericia.getEstado().getNombre() );
	}

