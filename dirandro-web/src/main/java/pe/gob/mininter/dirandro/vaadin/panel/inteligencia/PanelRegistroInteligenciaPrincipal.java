	public void postConstruct(){
		limpiar();
		cmbExpediente.setInputPrompt("Expediente");
		cmbExpediente.setItemCaptionPropertyId("autogenerado");
		cmbExpediente.setContainerDataSource(new BeanItemContainer<Expediente>(Expediente.class, expedienteService.buscar(null)));
		cmbExpediente.setImmediate(true);
		
		cmbExpediente.addListener(new ValueChangeListener() {
			
			private static final long serialVersionUID = -6010483113909217183L;

			@Override
			public void valueChange(ValueChangeEvent event) {
				lblExpediente.setValue("Nro de Expediente : "+((Expediente) cmbExpediente.getValue()).getAsunto());
			}						
		});
		
		btnGrabar.addListener((ClickListener)this);
		btnLimpiar.addListener((ClickListener)this);
	}
	
	@Override
	public void buttonClick(ClickEvent event) {
		if(event.getButton().equals(btnGrabar)) {
			grabarDocumentoInteligencia();
		}
		if(event.getButton().equals(btnLimpiar)) {
			limpiar();
		}
	}
	
	private void grabarDocumentoInteligencia() {
		Inteligencia inteligencia = new Inteligencia();
		inteligencia.setExpediente(cmbExpediente.getValue() != null ? (Expediente) cmbExpediente.getValue() : null);
		inteligencia.setFechaApertura((Date) dtFechaApertura.getValue());
		inteligencia.setNombre(txtNombre.getValue().toString());
		inteligenciaService.registrarDocInteligencia(inteligencia);
		lblAutogenerado.setValue("Nro Autogenerado : "+inteligencia.getAutogenerado());
		padre.habilitarTab(true);
		padre.setInteligencia(inteligencia);
		
	}

